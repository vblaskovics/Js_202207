// Names can contain letters
var aasd = 1;
console.log(aasd);

// Names can contain digits
var a200 = 2;
console.log(a200);

// Names can contain underscore
var a_b = 3;
console.log(a_b);

// Names must begin with a letter
// var 2sdfds = 4; --> error!

// Names can also begin with $ and _
var $a = 5;
var _b = 6;
console.log($a);
console.log(_b);

// Names are case sensitive
var aaa = 7;
var AAA = 8;
console.log(aaa);
console.log(AAA);

// Reserved words
// var var = 9; --> error

